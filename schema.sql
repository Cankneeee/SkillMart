CREATE OR REPLACE FUNCTION "public"."similar_listings"("listing_id" "uuid", "similarity_threshold" double precision, "max_results" integer) RETURNS TABLE("id" "uuid", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
DECLARE
  query_embedding VECTOR(1536);
BEGIN
  -- Get the embedding for the source listing
  SELECT embedding INTO query_embedding
  FROM listing_embeddings
  WHERE id = similar_listings.listing_id;
  
  -- Find similar listings
  RETURN QUERY
  SELECT
    l.id,
    1 - (le.embedding <=> query_embedding) as similarity
  FROM
    listing_embeddings le
  JOIN
    listings l ON l.id = le.id
  WHERE
    l.id != similar_listings.listing_id
    AND 1 - (le.embedding <=> query_embedding) > similarity_threshold
  ORDER BY
    similarity DESC
  LIMIT max_results;
END;
$$;


