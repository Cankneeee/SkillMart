CREATE OR REPLACE FUNCTION "public"."match_listings"("query_embedding" "public"."vector", "similarity_threshold" double precision, "max_results" integer) RETURNS TABLE("id" "uuid", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    l.id,
    1 - (le.embedding <=> query_embedding) as similarity
  FROM
    listing_embeddings le
  JOIN
    listings l ON l.id = le.id
  WHERE
    1 - (le.embedding <=> query_embedding) > similarity_threshold
  ORDER BY
    similarity DESC
  LIMIT max_results;
END;
$$;


ALTER FUNCTION "public"."match_listings"("query_embedding" "public"."vector", "similarity_threshold" double precision, "max_results" integer) OWNER TO "postgres";


CREATE OR REPLACE FUNCTION "public"."match_reviews"("query_embedding" "public"."vector", "similarity_threshold" double precision, "max_results" integer, "listing_id" "uuid") RETURNS TABLE("id" "uuid", "similarity" double precision)
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  RETURN QUERY
  SELECT
    r.id,
    1 - (re.embedding <=> query_embedding) as similarity
  FROM
    review_embeddings re
  JOIN
    reviews r ON r.id = re.id
  WHERE
    r.listing_id = match_reviews.listing_id
    AND 1 - (re.embedding <=> query_embedding) > similarity_threshold
  ORDER BY
    similarity DESC
  LIMIT max_results;
END;
$$;


