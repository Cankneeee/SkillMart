CREATE OR REPLACE FUNCTION "public"."mark_summary_for_update"() RETURNS "trigger"
    LANGUAGE "plpgsql"
    AS $$
BEGIN
  -- Set the needs_update flag to true for the associated listing
  UPDATE listing_summaries
  SET needs_update = TRUE, updated_at = NOW()
  WHERE listing_id = COALESCE(
    NEW.listing_id, 
    OLD.listing_id, 
    NEW.id, 
    OLD.id
  );
  
  RETURN NEW;
END;
$$;


